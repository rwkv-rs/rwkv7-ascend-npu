def materialize_marlin_sources() -> Path:
    """Extract and return a stable cached source directory."""

    payload = base64.b85decode(_BUNDLE_B85.encode("ascii"))
    if hashlib.sha256(payload).hexdigest() != _BUNDLE_SHA256:
        raise RuntimeError("RWKV7 Marlin source bundle checksum mismatch")
    cache_base = Path(
        os.environ.get(
            "RWKV7_MARLIN_SOURCE_CACHE",
            Path.home() / ".cache" / "rwkv7_hf" / "marlin_sources",
        )
    ).expanduser()
    target = cache_base / _BUNDLE_SHA256[:16]
    marker = target / ".complete"
    if marker.is_file() and all((target / name).is_file() for name in _BUNDLE_FILES):
        return target

    cache_base.mkdir(parents=True, exist_ok=True)
    if target.exists() and not marker.is_file():
        shutil.rmtree(target, ignore_errors=True)
    temporary = Path(tempfile.mkdtemp(prefix="marlin-", dir=cache_base))
    try:
        with zipfile.ZipFile(io.BytesIO(payload)) as archive:
            archive.extractall(temporary)
        (temporary / ".complete").write_text(_BUNDLE_SHA256 + "\n", encoding="utf-8")
        try:
            temporary.replace(target)
        except OSError:
            if not marker.is_file():
                raise
    finally:
        if temporary.exists():
            shutil.rmtree(temporary, ignore_errors=True)
    if not all((target / name).is_file() for name in _BUNDLE_FILES):
        raise RuntimeError("RWKV7 Marlin source bundle extraction is incomplete")
    return target


